% Part a
t = 0:.1:200;
R=10;
r=1.8;
d=0.8;
a = .5;
x = (R-r).*cos(t)+d.*cos(((R-r)/r).*t);
y = ((R-r).*sin(t)+d.*sin(((R-r)/r).*t)).*cos(a.*t);
z = ((R-r).*sin(t)+d.*sin(((R-r)/r).*t)).*sin(a.*t);
plot3(x,y,z);
% Part b
figure 
plot3(x(1:400),y(1:400),z(1:400),'y');
hold on
plot3(x(401:800),y(401:800),z(401:800),'r');
plot3(x(801:1200),y(801:1200),z(801:1200),'g');
plot3(x(1201:1600),y(1201:1600),z(1201:1600),'m');
plot3(x(1601:2000),y(1601:2000),z(1601:2000),'c');